                        <p><u>Lichtinval, voor het juist detecteren van gezichten gedurende de hele dag:</u>
                        </p>
                        <p>Bij het lopen vanaf de trap naar de applicatie zit de beweging tussen de 600/800.
                            Bij het staan voor de applicatie vindt er een beweging plaats van tussen de 100/200.
                            Hoe dichter de respondent bij het scherm staat, hoe minder beweging er nodig is voor een hogere mate van beweging. Wanneer respondenten voor de camera staan en bewegingen zit de beweging tussen de 200/800.
                        </p>
