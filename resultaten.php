                        <h4>7.4 Semigestructureerde interviews </h4>
                   <p>De resultaten uit Bijlage 10 tonen dat: </p>
                        <ul>
                            <li>De applicatie duidelijk is, het toevoegen van titels/bijschriften zou bijdrage aan de duidelijkheid van de grafiek, tabel en gauge.</li>
                            <li>De applicatie als interessant/leuk wordt beschouwen, dit omdat er gebruik wordt gemaakt van nieuwe technologieën en bewegende elementen. </li>
                            <li>De begroeting als leuk wordt beschouwen, echter zou deze nog uitgebreid kunnen worden met:</li>
                            <li>-	aanpassing aan de hand van feestdagen;</li>
                            <li>-	welkom heten op basis van tijdstip van de dag;</li>
                            <li>-	reageren op moment van herkenning;</li>
                            <li>-	motiverende woorden wanneer afspraken naderen, aan de hand van een agenda koppeling. </li>
                            <li>De mate van beweging wordt op een leuke manier weergegeven, echter ontbreken de titels wat de grafiek en gauge moeten verduidelijken.</li>
                            <li>Het weergeven van de mate van glimlach door voorbijgangers als leuk wordt beschouwen, echter zien ze liever dat er een emoji wordt weergegeven dan een percentage van de mate van je glimlach. </li>
                            <li>Dat het gebruik van stem/spraak een goede toevoeging is als extra interactie. </li>
                            <li>De meest gegeven tip, het verduidelijken van de interface is.</li>
                            <li>De verzamelde data voor meer doeleinden kan worden gebruikt:</li>
                            <li>-	interactieve paskamer</li>
                            <li>-	inchecken bij restaurants, openbaar vervoer</li>
                            <li>-	gepersonaliseerde cadeautjes/reclame</li>
                            <li>-	betalingen uitvoeren </li>
                            <li>-	openen van gesloten elektrische sloten</li>
                            <li>-	aanwezigheid check, in en uitklokken</li>
                            <li>-	tevredenheidsonderzoek</li>
                            <li>-	interactieve spelletjes</li>
                        </ul>
