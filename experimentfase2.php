                        <h1>6.1.3 Experimenteel onderzoek</h1>
                     <p>Adwise wilt dat 75% van de personen in het model correct herkent worden. Dit moet gelden voor zowel vrouw als man en gedurende de hele dag. Om dit te bereiken is het volgende getest:</p>
