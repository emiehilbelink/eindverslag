                            </li>
                        </ul>
                      <p><br>De uiteindelijke herkenning van personen in het model wordt pas onderzocht nadat de juiste lichtinval en de plaatsing van de camera zijn verwerkt in het prototype. Dit omdat deze resultaten invloed hebben op het herkennen van voorbijgangers. Als de applicatie onjuist is afgesteld en opgezet, zou de herkenning slechter of niet plaatsvinden, dan wanneer deze juist is opgezet met de de juiste afstellingen.
                      </p>
                        <h4>6.1.3.1 Beschrijving onderzoeksprocedure
                        </h4>
                        <p>Om de positie van de camera te bepalen, moeten de volgende stappen doorlopen worden:</p>
                        <p><b>1. </b>De onderzoeker plaatst de camera zowel voor het linker als het rechter scherm.
                        <br><b>2. </b>De onderzoeker plaatst 1 respondent op de trap en 1 respondent voor het scherm waar de camera hangt.
                         </p>
                        <p>Dit moet worden gedaan voordat de lichtinval wordt bepaald. Dit omdat de plek van de camera invloed heeft op de lichtinval. Wanneer de bovenstaande stappen succesvol afgerond zijn, kan de lichtinval onderzocht worden:
                        </p>
                            <p><b>1. </b>De onderzoeker laat 2 respondenten van de trap naar het scherm.
                            <br><b>2. </b>De onderzoeker verstelt de lichtinval van de camera.
                            <br><b>3. </b>De onderzoeker noteert wanneer er wel of geen herkenning plaatsvindt.
                        </p>
                        <p>Het bepalen van de lichtinval draagt bij aan het detecteren en herkennen van gezichten. Hierbij wordt de helderheid niet lager dan 100 gezet. Dit omdat dit het scherm zichtbaar donkerder maakt, wat meteen geen detectie oplevert. Daarnaast kan hiermee de gemiddelde mate van beweging worden vastgesteld.
                        </p>
                        <p>Nadat bovenstaande aspecten bepaald zijn worden 5 vrouwen en 5 mannen toegevoegd in het model. Voor het toevoegen van personen, heeft de onderzoeker referentiemateriaal nodig. Dit referentiemateriaal bestaat uit eigen gemaakte afbeeldingen en afbeeldingen welke de personen aanleveren. Het eigengemaakte referentiemateriaal wordt zo gemaakt, dat het ideaal is voor de situatie waar dit onderzoek gaat plaatsvinden. </p>
                        <p>De personen komen zo goed als recht op de camera af als ze vanaf de trap komen. Vandaar dat er afbeeldingen worden gemaakt waarbij de camera recht voor het gezicht wordt geplaatst. Hierbij hebben de personen mogelijk, zowel los als vast haar, zowel bril op als bril af etc. Daarnaast worden er ook ook afbeeldingen van de zijkant van het gezicht gemaakt, zodat wanneer medewerkers schuin langs de schermen lopen, er mogelijk ook een herkenning kan plaatsvinden. </p>
                        <p>
                            <b>1. </b>De onderzoeker zorgt voor referentiemateriaal, ideaal voor de situatie waar het onderzoek plaatsvindt.
                            <br><b>2. </b>Het referentiemateriaal wordt door de detectie service gehaald, welke bij gezichtsdetectie de gedetecteerde gezichten teruggeeft.
                            <br><b>3. </b>De onderzoeker voegt 5 mannen en 5 vrouwen toe in het model. Zie Paragraaf 6.1.3.2.
                            <br><b>4. </b>De onderzoeker laat de toegevoegde personen voor het scherm staan.
                            <br><b>5. </b>De personen staan zowel gezamenlijk, als alleen lang het scherm.
                        </p>
<p>Aan de hand hiervan kan worden gekeken hoeveel % van de personen in het model correct herkent worden. Het tijdstip van de dag heeft geen invloed op de applicatie, dit omdat er het licht alleen wordt bepaald door lampen, die de gehele dag aan staan. </p>
                        <h4>6.1.3.2 Onderzoekspopulatie en steekproefgroote</h4>
                        <p>Aangezien de beperkte tijd en de tijd die het toevoegen van personen en het testen van deze personen inneemt, is er gekozen voor kwalitatief onderzoek. Volgens Topscriptie(z.j, 2013) zijn steekproeven van 10,12,15 of 20 respondenten gebruikelijk bij kwalitatief praktijkonderzoek. Omdat er zowel vrouwen als mannen werkzaam zijn bij Adwise, is in overleg met Adwise gekozen om zowel 5 man als vrouw toe te voegen in het model. Hiermee zit Adwise aan de minimale groepsgrootte van kwalitatief onderzoek. </p>
